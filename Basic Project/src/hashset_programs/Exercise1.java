package hashset_programs;

import java.util.HashSet;

public class Exercise1 {
	public static void main(String[] args) {
		HashSet<String> hs=new HashSet<String>();
		hs.add("Red");
		hs.add("Black");
		hs.add("Blue");
		hs.add("Green");
		hs.add("Violet");
		System.out.println("The hashset is : " + hs);
	}

}
