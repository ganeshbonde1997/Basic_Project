public class ArrayEx {
    //Display an array using for loop
    public static void main(String[] args) {
        //Declare and Intialize array
        int a[] = {12,23,28,32,25};
        //Display the array list using for loop
        for(int i = 0; i<a.length; i++){
            System.out.println("The elements of an array : " + a[i]);
        }
    }
}
